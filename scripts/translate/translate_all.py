def process_file(source_filename, translations, output_filename):
    """Read source JSON, verify all entries are covered, write translation."""
    source_path = os.path.join(BASE_IN, source_filename)
    output_path = os.path.join(BASE_OUT, output_filename)

    with open(source_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    entries = data["entries"]

    result = {}
    missing = []
    for entry in entries:
        name = entry["name"]
        en_text = entry["strings"].get("en", "")
        if name in translations:
            result[name] = translations[name]
        else:
            missing.append((name, en_text))

    if missing:
        print(f"WARNING: {len(missing)} entries missing translations in {source_filename}:")
        for name, en in missing:
            print(f"  {name}: {repr(en)}")

    with open(output_path, "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    print(f"Wrote {len(result)} entries to {output_path}")
    print(f"Total entries in source: {len(entries)}")
    return len(entries), len(result), len(missing)


